mood = input("What mood are you? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don't cry please")
elif mood == "excited":
    print("Calm down please")
elif mood == "relaxed":
    print("Nice to see you relaxed")
else:
    print("I don't recognize this mood")
